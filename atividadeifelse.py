#Faça um programa que o usuario digite um numero e diga se o numero é superior a 20, inferior a 10 ou se esta dentre 10 e 20 
num = int(input("Digite um número inteiro: "))
if num > 20:
    print("Este número é superior que 20")
elif num < 10:
    print("Este número é inferior à 10")
else:
    print("Este número esta dentre 10 e 20") 
